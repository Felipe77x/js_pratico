/* 
Const.
Quando precisamos armazenar um valor que não deve ser alterado, podemos usar uma constante em JavaScript. 
Assim como uma variável, uma constante guarda um valor temporariamente na memória, porém esse valor é fixo e não pode ser modificado. 
Ao tentar alterar o valor de uma constante, um erro será gerado. 
É útil utilizar constantes quando queremos garantir que um valor não seja alterado. 

*/





// Const 

//  Uma constante cria uam variável cujo valor é fixo (não pode ser alterado)

const nome =  "Felipe Dias"

console.log(nome)

// Irá gerar um erro porque o valor não pode ser alterado

nome =  "Felipe D"

console.log(nome)






