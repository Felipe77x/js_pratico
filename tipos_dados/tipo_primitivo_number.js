/*   

Number
O tipo NUMBER é utilizado para lidar com números em programação. 
Podemos armazenar valores inteiros positivos, inteiros negativos e números reais, também conhecidos como float. 
É importante usar o ponto como separador decimal, não a vírgula. 
Tentar dividir um número por um texto resulta em "NaN", que significa "Not a Number". 
O tipo NUMBER é essencial para lidar com diferentes tipos de números em programação.
*/


// Number

// Verificando o tipo do número
console.log(typeof 5); // "number"

// Inteiro positivo
console.log(5); // 5

// Inteiro negativo
console.log(-5); // -5

// Número real ou float
console.log(125.70); // 125.7

// NaN - Not a Number
console.log(12.5 / "rodrigo"); // NaN
