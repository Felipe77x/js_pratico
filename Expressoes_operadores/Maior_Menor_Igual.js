//  Maior, Menor e Igual

let salario = 709

let salario2 = 1500

// > Maior que 

console.log(salario2 > salario)


//  < Menor que 

console.log(salario2  < salario)

// >= Maior ou igual a

salario = 1500

console.log(salario >= salario2)

// =< Menor ou igual a

salario2 = 800

console.log(salario <= salario2)