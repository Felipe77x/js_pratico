/*  
expliquei os operadores de incremento e decremento em JavaScript. 
Demonstrei como usar o operador de incremento (++) e decremento (--) 
antes e depois de uma variável, mostrando como eles afetam o valor da variável. 
Também destaquei a diferença entre incrementar e decrementar antes e depois da variável, 
além de mostrar como adicionar ou subtrair valores maiores do que 1. 
Os operadores facilitam a manipulação de variáveis de forma mais eficiente.
*/

// incremento

incremento_number = 10


console.log(incremento_number++)


// Decremento

decremento_number = 11

console.log(decremento_number--)


// Incrementar mais de um (colocando)

incrementar_mais_valores = 70
incrementar_mais_valores+= 30

console.log(incrementar_mais_valores)

// Decrementar mais de um (removendo)

decrementarMaisValores = 85

decrementarMaisValores -= 55
console.log(decrementarMaisValores )

